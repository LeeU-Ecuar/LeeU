package algorithm;


import java.util.Date;
import java.util.Stack;

/**
 * 给定一个N*N的矩阵matrix，只有0和1两种值，返回边框全是1的最大正方形的边长长度。
 * 例如:011110100101001
 * 01111I01011
 * 其中边框全是1的最大正方形的大小为4*4，所以返回4。
 */
public class Matrix {



    public static void main(String[] args) {
        Stack sss = new Stack();
        sss.isEmpty();
        sss.push("1");
        sss.push("2");
        sss.pop();
        sss.push(new Date());
        System.out.println(sss.peek());
    }

}
